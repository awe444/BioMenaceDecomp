/* ========================================================================
 * Module state
 * ======================================================================== */

static SDL_Window *txt_window;
static SDL_Renderer *txt_renderer;
static SDL_Texture *txt_texture;
static uint8_t txt_buffer[TXT_BUF_SIZE]; /* 4000 bytes: char+attr pairs */
static int txt_initialized;

/* ========================================================================
 * Public API
 * ======================================================================== */

void TXT_Init(void)
{
    if (txt_initialized)
        return;

    memset(txt_buffer, 0, sizeof(txt_buffer));

    if (SDL_WasInit(SDL_INIT_VIDEO) == 0)
    {
        if (SDL_Init(SDL_INIT_VIDEO) != 0)
        {
            fprintf(stderr, "TXT_Init: SDL_Init failed: %s\n", SDL_GetError());
            return;
        }
    }

    txt_window = SDL_CreateWindow("Bio Menace",
        SDL_WINDOWPOS_CENTERED, SDL_WINDOWPOS_CENTERED,
        SCREEN_W * SCALE, SCREEN_H * SCALE,
        SDL_WINDOW_SHOWN);
    if (!txt_window)
    {
        fprintf(stderr, "TXT_Init: SDL_CreateWindow failed: %s\n", SDL_GetError());
        return;
    }

    txt_renderer = SDL_CreateRenderer(txt_window, -1,
        SDL_RENDERER_ACCELERATED | SDL_RENDERER_PRESENTVSYNC);
    if (!txt_renderer)
        txt_renderer = SDL_CreateRenderer(txt_window, -1, 0);
    if (!txt_renderer)
    {
        fprintf(stderr, "TXT_Init: SDL_CreateRenderer failed: %s\n", SDL_GetError());
        SDL_DestroyWindow(txt_window);
        txt_window = NULL;
        return;
    }

    SDL_RenderSetLogicalSize(txt_renderer, SCREEN_W, SCREEN_H);

    txt_texture = SDL_CreateTexture(txt_renderer,
        SDL_PIXELFORMAT_ARGB8888,
        SDL_TEXTUREACCESS_STREAMING,
        SCREEN_W, SCREEN_H);
    if (!txt_texture)
    {
        fprintf(stderr, "TXT_Init: SDL_CreateTexture failed: %s\n", SDL_GetError());
        SDL_DestroyRenderer(txt_renderer);
        txt_renderer = NULL;
        SDL_DestroyWindow(txt_window);
        txt_window = NULL;
        return;
    }

    txt_initialized = 1;

#ifdef __ANDROID__
    // On Android, make the text screen fullscreen too so the hardware
    // setup screen fills the display instead of appearing tiny.
    SDL_SetWindowFullscreen(txt_window, SDL_WINDOW_FULLSCREEN_DESKTOP);
#endif
}

void TXT_Update(void)
{
    uint8_t *pixels;
    int pitch;
    int row, col, y;

    if (!txt_initialized || !txt_texture)
        return;

    /* Process any pending SDL events to keep the window responsive */
    {
        SDL_Event ev;
        while (SDL_PollEvent(&ev))
        {
            if (ev.type == SDL_QUIT)
            {
                TXT_Shutdown();
                exit(0);
            }
        }
    }

    if (SDL_LockTexture(txt_texture, NULL, (void **)&pixels, &pitch) != 0)
        return;

    for (row = 0; row < TXT_ROWS; row++)
    {
        for (col = 0; col < TXT_COLS; col++)
        {
            int idx = (row * TXT_COLS + col) * 2;
            uint8_t ch   = txt_buffer[idx];
            uint8_t attr = txt_buffer[idx + 1];
            uint8_t fg   = attr & 0x0F;
            uint8_t bg   = (attr >> 4) & 0x0F;

            const uint8_t *glyph = &cp437_font_8x16[ch * CHAR_H];

            for (y = 0; y < CHAR_H; y++)
            {
                uint8_t bits = glyph[y];
                int px_y = row * CHAR_H + y;
                uint8_t *dest = pixels + px_y * pitch + col * CHAR_W * 4;
                int x;

                for (x = 0; x < CHAR_W; x++)
                {
                    const uint8_t *color = (bits & (0x80 >> x))
                                         ? dos_palette[fg]
                                         : dos_palette[bg];
                    dest[x * 4 + 0] = color[2]; /* B */
                    dest[x * 4 + 1] = color[1]; /* G */
                    dest[x * 4 + 2] = color[0]; /* R */
                    dest[x * 4 + 3] = 0xFF;     /* A */
                }
            }
        }
    }

    SDL_UnlockTexture(txt_texture);
    SDL_RenderClear(txt_renderer);
    SDL_RenderCopy(txt_renderer, txt_texture, NULL, NULL);
    SDL_RenderPresent(txt_renderer);
}

void TXT_Shutdown(void)
{
    if (txt_texture)
    {
        SDL_DestroyTexture(txt_texture);
        txt_texture = NULL;
    }
    if (txt_renderer)
    {
        SDL_DestroyRenderer(txt_renderer);
        txt_renderer = NULL;
    }
    /* If TXT_TransferWindow() was called, txt_window is already NULL
     * and this block is a no-op.  Otherwise destroy the unclaimed window. */
    if (txt_window)
    {
        SDL_DestroyWindow(txt_window);
        txt_window = NULL;
    }
    txt_initialized = 0;
}

uint8_t *TXT_GetBuffer(void)
{
    if (!txt_initialized)
        TXT_Init();
    return txt_buffer;
}

void *TXT_TransferWindow(void)
{
    SDL_Window *w = txt_window;

    /* Clear the window pointer so TXT_Shutdown() won't destroy it */
    txt_window = NULL;

    return w;
}
